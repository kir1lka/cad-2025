CREATE TABLE CATEGORIES (
    ID BIGINT PRIMARY KEY,
    NAME VARCHAR(255),
    DESCRIPTION VARCHAR(255)
);

CREATE TABLE PRODUCTS (
    ID BIGINT PRIMARY KEY,
    NAME VARCHAR(255),
    DESCRIPTION VARCHAR(255),
    PRICE DOUBLE,
    STOCK_QUANTITY INT,
    CATEGORY_ID BIGINT,
    FOREIGN KEY (CATEGORY_ID) REFERENCES CATEGORIES(ID)
);